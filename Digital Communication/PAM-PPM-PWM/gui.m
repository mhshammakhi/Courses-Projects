function calculate_Callback(hObject, eventdata, handles)
% hObject    handle to calculate (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
run code;
hal = get(handles.mod,'value');
switch hal
    case 1
     for n=1:b:length(h)-b;
            num=num+1;
            w=bin2dec(h(n:n+b-1));
            w=w*ones(1,50);
            bit=[bit,w];
     end
        plot(bit,'LineWidth',1.5);grid on;
        axis([0 50*num 0 lay]);
        n=2;
    case 2
     for n=1:b:length(h)-1;
            num=num+1;
            w=bin2dec(h(n:n+b-1));
            w=[ones(1,5*(1+floor(3*w))),zeros(1,5*(3*lay+3-floor(3*w)))];
            bit=[bit,w];
     end
        plot(bit,'LineWidth',1.5);grid on;
        axis([0 5*(3*lay+4)*num 0 1.5]);
    case 3
      for n=1:b:length(h)-1;
            num=num+1;
            w=bin2dec(h(n:n+b-1));
            w=[zeros(1,20*w),ones(1,20),zeros(1,20*(lay-w))];
            bit=[bit,w];
     end
        plot(bit,'LineWidth',1.5);grid on;
        axis([0 20*(lay+1)*num 0 1.5]);
     end

        



